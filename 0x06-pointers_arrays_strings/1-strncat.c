#include "main.h"

/**
 * _strncat - joins two strings with n bytes
 * function- main
 * description: the starting of while loop
 * @dest: copy to
 * @src: copy from
 * @n: number of char to be copied
 * Return: dest
 */
char *_strncat(char *dest, char *src, int n);
{
int i;
int j;
i = 0;

int main(void)
{
while (dest[i] != '\0')
{
i++;

}
j = 0;

while (j < n && src[j] != '\0')
{
dest[i] = src[j];
i++;
j++;
}
dest = '\0';
return (dest);
}
}
